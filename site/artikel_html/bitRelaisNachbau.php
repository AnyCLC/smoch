    <p>Die Schaltung handelt sich um eine einfache, wenn auch <i>stromfressende</i> Variante.</p>
    <p>Dabei wird der Strom, der beim angezogenen Relais fließt, gleich wieder verwendet, um das Relais «einzuschalten»; so bleibt es nach einmaligem Schalten <i>eingeschnappt</i>, wie ein Schnappschloss, eben.</p>    
                                                                                                                             <p>Um das Bit wieder zu löschen muss die Zuleitung unterbrochen werden (Aus-Taster in der Grafik). Dies geschieht in unserer Schaltung einfach mit einem Taster; in einem Schaltnetz, wird jedoch dazu i.&nbsp;d.&nbsp;R. ein anderes Relais verwendet.</p>


<h3>Verwendung</h3>
<p>Das Relais-Latch wird verwendet, um ein einzelnes Bit zu speichern.</p>

