<h3>Funktionsweise</h3>
<p>
Die Informationen der CD, das sogenannte „Programm“, sind auf einer spiralförmig nach außen verlaufenden Spur angeordnet; sie belegen maximal 85 % der
CD-Gesamtfläche. Der Programmbereich reflektiert Licht mit deutlichen Farberscheinungen wegen seiner Mikrostruktur, den Pits. Länge und Abstand dieser
kleinen Vertiefungen bilden einen seriellen digitalen Code, der die gespeicherte Information repräsentiert. Auf einer Audio-CD können maximal 99 Musiktitel
gespeichert werden; dazu hat jede Scheibe ein Inhaltsverzeichnis (TOC, table of contents) und einen der Information eingelagerten Zeitcode. Texteinblendungen
und weitergehende Informationen können optional aufgebracht werden. Die Abtastung der CD erfolgt kontaktlos über einen der Spur nachgeführten Laser-Interferenzdetektor
von der spiegelnden Unterseite her. Die Geschwindigkeit, mit der die Daten eingelesen werden, hängt von der Drehzahl der CD ab; diese wird traditionellerweise
so geregelt, dass eine vorgegebene Datenrate eingehalten wird. Die Datenspur hat eine konstante Bahngeschwindigkeit (engl. constant linear velocity, CLV). Dadurch ergibt sich bei nach außen fahrendem Abtastsystem eine Drosselung der Drehzahl. Bei Verfahren ähnlich der
Analogschallplatte spricht man hingegen von konstanter Winkelgeschwindigkeit (CAV).
</p>
