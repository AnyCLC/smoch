<h3>Verwendung</h3>
<p>
Die CD wurde in ganz vielen verschiedenen Einsatzgebieten verwendet.
</p>
<ul>
<li>CD als Backup: <br>Die CD wurde als Backup-Medium verwendet um Daten langfristig sichern zu können.</li>
<li>CD als Programmspeicher: <br> Die Anwendung ein Programm auf einer CD zu speichern und von dort aus das Programm auf einen Computer zu instalieren hat sich bis heute gehalten.</li>
<li>CD als Musikspeichermedium: <br> Die CD löste die Kasette als Musikspeichermedium ab.</li>
<li>CD als Filmspeichermedium: <br> Die CD wurde in einer weiterentwickelten Form auch als Speichermedium für Filme verwendet.</li>
</ul>

