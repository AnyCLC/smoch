<h3>Funktionsweise</h3>
<p> Über die Anschlusspins, wird eine Spannung in den Schaltkreis gegeben. Je nach Funktion des Chips, wird die Spannung, dann über einen anderen oder auch mehrere Pins wieder ausgegeben. </p>

<h3>Verwendung</h3>
     <p>Integrierte Schaltkreise finden diverse Anwendungen; Speichern, rechnen, ... Im Museum haben sogar einen Chip, der abhängig vom Input verschiedene Spannungen liefern kann.</p>



