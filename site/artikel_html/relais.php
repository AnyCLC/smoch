<h3>Funktionsweise</h3>
<p>
Elektronik Relais finden sich nicht nur in den Schaltschränken großer Maschinenhallen, sondern haben sich bis in die elektronische Steuerung von Fahrzeugen hinein verbreitet. Zitat: https://www.schukat.com/schukat/schukat_cms_de.nsf/index/CMS3287CF7DA43BC106C125759000449692<br>
Ein Computer ist ein Rechner, welcher Programmgesteuert wird. Ein Relaiscomputer ist ein Computer, welcher aus Relais besteht. Der Computer, der allerersten Generation, wurde aus Relais gebaut. Im Vergleich zu Heute, waren die Relais damals viel grösser, manche waren so gross wie ein Schrank.<br>
Relais sind heutzutage durch Transistoren und Chips (ICs) aus der Elektronik weitestgehend verdrängt. Chips und Transistoren bieten Vorteile, wie geringe Leistungsaufnahme, enorme Geschwindigkeit und hohe Packungsdichte, was in der Computertechnik von grosser Bedeutung ist. Relais dagegen sind recht langsam, brauchen zudem viel Strom.<br>
</p>
<h3>Verwendung</h3>
<p>
Relais sind elektromechanische Bauelemente. Sie werden hauptsächlich für die folgenden Anwendungsfälle eingesetzt: 
<ul>
	<li>Zum gleichzeitigen und potentialgetrennten Schalten mehrerer Laststromkreise mit nur einem Steuerstromkreis</li>
	<li>Zum Schalten von hohen elektrischen Leistungen mit niedriger Leistung (Schaltverstärker)</li>
	<li>Um eine galvanische Trennung zwischen steuerndem und zu schaltendem Stromkreis zu erreichen</li>
	<li>Um geringe Schaltübergangswiderstände im geschlossenen Zustand des Kontaktes bei gleichzeitig sehr großem Kontaktübergangswiderstand im geöffneten Zustand zu erreichen</li><br>
</ul>
Zitat: https://de.wikipedia.org/wiki/Relais
</p>
