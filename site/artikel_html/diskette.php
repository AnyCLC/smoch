<h3>Verwendung</h3>
<p>
Die Diskette wird Hauptsächlich in der Datenübertragung und Sicherung verwendet.
Heute kommt sie bis auf ein paar wenige Ausnahmen nicht mehr  zur Verwendung.
Sie wurde von der CD, USB-Stick und ähnlichen Speichermedien abgelöst.
</p>
