<h3>Funktionsweise</h3>
<p>
Die Diskette beinhaltet eine Magnetscheibe, diese ist eine dünne, biegsame Kunststoffscheibe aus boPET. 
Diese Scheibe ist mit einem magnetisierbaren Material, meistens Eisenoxid, beschichtet, dessen Speicherprinzip dem der 
Festplatte ähnelt. Anfangs nur von einer quadratischen Papphülle mit Lesefenster umschlossen ist ein 3,5″ Datenträger von einem festen rechteckigen Kunststoffgehäuse
mit Fensterverschluß besser geschützt. Die englische Bezeichnung „floppy disk“ oder kurz „Floppy“, was ungefähr mit „wabbelige Scheibe“ ins Deutsche zu
übertragen wäre, beruht auf der flexiblen Eigenschaft des Speichermediums im Gegensatz zur hard disk („feste Scheibe“) eines 
Festplattenlaufwerkes (englisch hard disk drive).

Im Unterschied zur Festplatte schleift der 
Magnetkopf (Schreib-Lese-Kopf) bei einer Diskette direkt auf der beschichteten magnetisierbaren Oberfläche, was mit der Zeit zu deutlichem Abrieb führt und die dauerhafte
Verwendbarkeit von Disketten stark einschränkt. Außerdem schleift auch die Magnetscheibe direkt an der umgebenden, fest im Laufwerk liegenden Hülle. Daher
wird diese Hülle auf der Innenseite mit 
Teflon-beschichtetem (oder ähnlichem) Gewebe ausgestattet, um die Reibung zu minimieren.
