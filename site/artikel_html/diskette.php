<h3>Aufbau</h3>
<p>
Die Diskette ist ein magnetischer tragbarer Datenträger mit einer Kapazität von 80 KB bis zu 3250 KB.

Das Format (im Sinne von physischer Größe) von Disketten wird vor allem in Zoll (″) angegeben, gebräuchliche Größen waren und sind:
</p>
 <ul>
<li>8″ (eigentlich 200 mm) mit 180 KB bei Disketten – das ursprüngliche Diskettenformat von IBM als Ersatz für unhandliche Lochkartenstapel, später mit bis zu 1 MB</li>
<li>5,25″ (eigentlich 130 mm) mit 80 bis 1200 KB (160 KB beim ersten IBM PC)</li>
<li>3,5″ (eigentlich 90 mm) mit 360 bis 1440 KB ≈ 1,44 MB (HD-Disketten, zuletzt Standardformat vieler Systeme), selten bis zu 3520 KB ≈ 3,4 MB (ED-Disketten mit 44 Sektoren formatiert) </li>
<li>3″ (eigentlich 80 mm) mit 180 KB pro Seite (u.a. Schneider/Amstrad CPC und Joyce)</li>
</ul>
