<h3>Funktionsweise</h3>
<p>
Ein Kernspeicher besteht im Wesentlichen aus einer großen Anzahl von magnetisierbaren, 
hartmagnetischen Ferrit-Kernen, welche zu Ringen geformt sind und daher als 
Ringkern bezeichnet werden. Hartmagnetisch bedeutet in diesem Zusammenhang, dass jeder Kern ein Datenbit in dem Vorzeichen der Remanenzflussdichte Br speichern kann. 
Zum Lesen bzw. Beschreiben der Kerne verlaufen mindestens zwei elektrische und gegenseitig isolierte Drähte durch die Ringöffnungen.
Der elektrische Strom Im im Schreibdraht muss zum Beschreiben so groß sein, dass die durch den Strom Im erzeugte 
magnetische Koerzitivfeldstärke Hc im magnetischen Kreis des Kernes überschritten wird. Dadurch kommt es in dem hartmagnetischen Werkstoff des Ringes, welcher eine fast quadratische 
Hystereseschleife aufweist, zu einer Speicherung des Zustandes im Vorzeichen der Remanenzflussdichte. Die Remanenzflussdichte kann zwei stabile Punkte annehmen, welche
in der Hystereseschleife mit Br und −Br bezeichnet sind.

Gleichzeitig mit dem Schreibvorgang wird in dem zweiten Draht, der Leseleitung, ein Spannungsimpuls induziert, anhand dessen die Orientierung der ursprünglich im Kern gespeicherten Remanenzflussdichte festgestellt werden kann. Die Information kann also nur destruktiv
ausgelesen werden. Der ggf. ummagnetisierte Kern muss dann neu beschrieben werden, um den ursprünglichen Dateninhalt wiederherzustellen.

War im Kern zuvor eine positive Remanenzflussdichte +Br gespeichert,
kommt es bei dem Einschreiben einer „0“ durch die große Änderung der magnetischen Flussdichte im Kern auf der Leseleitung zu einem Spannungsimpuls Ul in
der Größenordnung von einigen 100 mV. In der Hysteresekurve links ist dick gezeichnet der Verlauf der Flussdichte verdeutlicht. Nach dem Abschalten des
Stromes Imin der Schreibleitung bleibt im Kern die Remanenzflussdichte −Br zurück, was dem Zustand „0“ entspricht. War im Kern zuvor bereits eine negative
Remanenzflussdichte −Br gespeichert, durchläuft die Flussdichte nur einen kleinen Teil der Hysteresekurve und die Änderungsrate ist minimal. Demzufolge
ist der Spannungsimpuls auf der Leseleitung ebenfalls minimal. In beiden Fällen befindet sich der Kern nach dem Auslesen im Zustand „0“, und der ursprüngliche
Speicherinhalt muss bei Bedarf durch einen inversen Strom −Im neu geschrieben werden.

Zusätzlich zu den Kernspeichern sind für den Betrieb Leseverstärker notwendig, welche die geringen Spannungsimpulse auf der Leseleitung in passende logische
Spannungspegel umwandeln. Zum Beschreiben sind 
Stromquellen notwendig.
