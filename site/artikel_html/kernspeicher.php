<h3>Funktionsweise</h3>
<p class='zit copy'>
Ein Kernspeicher besteht im Wesentlichen aus einer großen Anzahl von magnetisierbaren Ferrit-Ringen.

Zum Lesen bzw. Beschreiben der Kerne verlaufen mindestens zwei elektrische und gegenseitig isolierte Drähte durch die Ringöffnungen.
     
Der elektrische Strom <b><i>Im</i></b> im Schreibdraht muss zum Beschreiben so groß sein, dass die durch den Strom <b><i>Im</i></b> erzeugte 
magnetische Koerzitivfeldstärke <b><i>Hc</i></b> im magnetischen Kreis des Kernes überschritten wird.

Dadurch kommt es in dem hartmagnetischen Werkstoff des Ringes, welcher eine fast quadratische 
Hystereseschleife aufweist, zu einer Speicherung des Zustandes im Vorzeichen der Remanenzflussdichte.

Die Remanenzflussdichte kann zwei stabile Punkte annehmen, welche in der Hystereseschleife mit <b><i>Br</i></b> und <b><i>−Br</i></b> bezeichnet sind.
<br />
Gleichzeitig mit dem Schreibvorgang wird in dem zweiten Draht, der Leseleitung, ein Spannungsimpuls induziert, anhand dessen die
Orientierung der ursprünglich im Kern gespeicherten Remanenzflussdichte festgestellt werden kann.
Die Information kann also nur destruktiv ausgelesen werden. Der ggf. ummagnetisierte Kern muss dann neu beschrieben werden, um den ursprünglichen Dateninhalt wiederherzustellen.

     War im Kern zuvor eine positive Remanenzflussdichte <b><i>+Br</i></b> gespeichert,
kommt es bei dem Einschreiben einer „0“ durch die große Änderung der magnetischen Flussdichte im Kern auf der Leseleitung zu einem Spannungsimpuls <b><i>Ul</i></b> in
der Größenordnung von einigen 100 mV.

Zusätzlich zu den Kernspeichern sind für den Betrieb Leseverstärker notwendig, welche die geringen Spannungsimpulse auf der Leseleitung in passende logische
Spannungspegel umwandeln. Zum Beschreiben sind 
Stromquellen notwendig.</p>
