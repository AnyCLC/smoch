<h3>Aufbau</h3>
<p>
Der Kernspeicher, Magnetkernspeicher oder auch Ferritkernspeicher (englisch magnetic-core memory oder englisch ferrite-core memory) ist eine frühe Form 
nichtflüchtiger Speicher mit wahlfreiem Zugriff von elektronischen Rechenmaschinen.
Er besteht aus auf Drähten aufgefädelten hartmagnetischen Ringkernen (englisch cores), die durch 
elektrische Ströme in den Drähten ummagnetisiert und ausgelesen werden können. Das Vorzeichen der 
magnetischen Remanenz der einzelnen Ringkerne repräsentiert deren Speicherinhalt.

Kernspeicher wurden etwa von 1950 bis 1980 in den damals üblichen Rechenmaschinen eingesetzt. Heute gibt es nur noch vereinzelte Anwendungsfälle, z. B.
in der Signaltechnik der Eisenbahn, wobei die Beschaffung von Ersatzteilen, selbst für einzelne „1 Bit“ Speicher zu einem großen Problem geworden ist.

In allgemeinerem Sinn wird der Begriff aber auch für Arbeitsspeicher in anderen, neueren Technologien verwendet, im Sinne von „Speicher im Kern des Rechners“.
