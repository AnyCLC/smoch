<h3>Aufbau</h3>
<p class='zit copy'>
Ein Halbaddierer (engl. half adder) ist ein Schaltnetz, das üblicherweise als digitale Schaltung realisiert wird. Er besteht aus zwei Eingängen und zwei Ausgängen.
<p class='zit src'>
<i>Quelle: <a href="https://de.wikipedia.org/wiki/Halbaddierer">Wikipedia</a> April 2018</i>

<h3>Funktionsweise</h3>
<p>
Der Halbaddierer liefert die Grundlage fürs Rechnen mit dem Computer. Er wird gebraucht um zwei einstellige Dualziffern zu addieren, vermutlich wurde er im Jahre 1937, im Zuse Z1 verwendet.
     <img src='../expo_images/halbaddierer.png' alt='Halbaddierer Schema aus einem XOR und einem AND.'  title='Schaltschema' />
</p>

<h3>Verwendung</h3>
<p class='zit copy'>
Halbaddierer sind häufig Bestandteil von Mikroprozessoren. Ebenso kann diese Logikfunktion in einem programmierbaren Logikbaustein (PLD), 
einem FPGA oder einem ASIC als Teil einer Gesamtschaltung realisiert werden. Mit diskreten Logikbauelementen wird diese 
Schaltungsfunktion heute kaum mehr realisiert, da mit diesen Bauelementen die erforderlichen meist hohen Taktfrequenzen nicht erreicht 
werden können und der Schaltungsaufwand für den Aufbau und die Verdrahtung viel zu groß ist.

Aus zwei Halbaddierern und einem zusätzlichen Oder-Gatter kann ein Volladdierer aufgebaut werden.

Der Halbaddierer wird in Kombination mit Volladdierern zum Aufbau von Addiernetzen verwendet. 
