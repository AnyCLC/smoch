<h3>Verwendung</h3>
<p class='zit copy'>
Bedeutung haben Halbleiter für die Elektrotechnik und insbesondere für die Elektronik, wobei die Möglichkeit, ihre elektrische Leitfähigkeit durch Dotierung zu beeinflussen, eine entscheidende Rolle spielt. Die Kombination unterschiedlich dotierter Bereiche, z. B. beim p-n-Übergang, ermöglicht sowohl elektronische Bauelemente mit einer richtungsabhängigen Leitfähigkeit (Diode, Gleichrichter) oder einer Schalterfunktion (z. B. Transistor, Thyristor, Photodiode), die z. B. durch Anlegen einer elektrischen Spannung oder eines Stroms gesteuert werden kann (vgl. Arbeitszustände in Metall-Isolator-Halbleiter-Struktur).
</p>
