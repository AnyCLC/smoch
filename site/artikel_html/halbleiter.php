<h3>Funktionsweise</h3>
<p class='zit copy'>
Silicium, auch Silizium, ist ein chemisches Element mit dem Symbol Si und der Ordnungszahl 14. Es steht in der 4. Hauptgruppe (Kohlenstoffgruppe), bzw. der 14. IUPAC-Gruppe, und der 3. Periode des Periodensystems der Elemente. In der Erdhülle ist es, auf den Massenanteil (ppmw) bezogen, nach Sauerstoff das zweithäufigste Element. <br><br>
Silicium ist ein klassisches Halbmetall, weist daher sowohl Eigenschaften von Metallen als auch von Nichtmetallen auf und ist ein Elementhalbleiter. Reines, elementares Silicium besitzt eine grau-schwarze Farbe und weist einen typisch metallischen, oftmals bronzenen bis bläulichen Glanz auf.<br><br>
Elementares Silicium ist für den menschlichen Körper ungiftig, in gebundener silicatischer Form ist Silicium für den Menschen wichtig. Der menschliche Körper enthält etwa 20 mg/kg Körpermasse Silicium; die Menge nimmt mit zunehmendem Alter ab.
</p>
