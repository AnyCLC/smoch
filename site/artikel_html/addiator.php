<h3>Funktionsweise</h3>
<p>
Die erste Urkundlich erwähnte Rechenmaschiene wurde 1623 von Wilhelm Schickard in einem Brief erwähnt. Man konnte diese Maschiene jedoch nie auffinden, da angenommen wurde, dass sie während seines Hausbrandes verloren ging.<br>
Sie wurde jedoch im Jahre 1960 rekonstruiert.<br>
Gottfried Wilhelm Leibniz war der Erste, der eine rechenfähige mechanische Rechenmaschine mit allen vier Grundrechenarten im Jahre 1673 gebaut hat.<br>
</p>
<h3>Verwendung</h3>
<p>
Die Benutzung der Maschine ist mit wenig Aufwand möglich und schwierige Berechnungen konnten einfach gelöst werden, vor allem in den Bereichen in welchen grössere Berechnungen von nöten waren wurde die Rechenmaschine eingesetzt.<br>
</p>
