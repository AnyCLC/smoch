<h3>Funktionsweise</h3>
<p>
Wenn der mittlere Metallstab nicht unter Strom ist,
kann kein Strom von links nach rechts fliessen und umgekehrt. 
Wenn der mittlere Metallstab unter Strom ist, kann Strom von links nach rechts fliessen. Die Spannung wird dabei sogar um ca. das 200fache verstärkt! 
</p>

<h3>Verwendung</h3>
<p>
Die Mainboard (auch Motherboard genannt) verwendet selbst Transistoren,
jeder Prozessor enthält tausende von Transistoren.
Jede Grafikkarte enthält somit auch tausende von Tronsistoren, der Arbeitsspeicher ebenso, da der Speicher von einem Mikroprozessor gesteuert wird.</p>

