<h3>Funktionsweise</h3>
<p>
Wenn der mittlere Metallstab nicht unter Spannung ist,
kann kein Strom von links nach rechts fliessen und umgekehrt. 
Wenn der mittlere Metallstab unter Spannung ist, kann Strom von links nach rechts fliessen. Der Strom wird dabei sogar um ca. das 200fache verstärkt! 
</p>

<h3>Verwendung</h3>
<p>
Das Mainboard (auch Motherboard genannt) verwendet selbst Transistoren,
jeder Prozessor enthält tausende von Transistoren.
Jede Grafikkarte enthält somit auch tausende von Transistoren.</p>

