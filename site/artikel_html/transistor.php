<h3>Aufbau</h3>

<p>Der Transistor hat drei Metallstäbe; einer Rechts, einer Links und einer in der Mitte.<br />
Der mittlere Stab ist im Inneren mit einer Art Wiederstand verbunden.
Die Stäbe links und rechts sind mit speziell behandeltem Silizium verbunden.</p>
