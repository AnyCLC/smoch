<h3>Aufbau</h3>
<p>
  Eine Festplatte ist ein Speichermedium um grosse Datenmengen abzuspeichern. Es gibt sie intern als Hauptspeichermedium jedes Computers, oder als externes Medium zur Datensicherung oder Datenauslagerung. Es gibt auch verschiedene Varianten der Festplatte.

  Die Bekannteste ist die HDD (Hard Disk Drive). Sie besitzt eine rotierende Magnetscheibe die durch einen sich bewegenden Lesekopf abgelesen wird, ähnlich wie bei einem Plattenspieler.

  In den Letzten Jahren kam die SSD (Solid-State-Drive) immer mehr auf, Sie besitzt keine bewegenden Teile mehr, sie benutzt einen Flashspeicher im Grunde ein grosser USB-Stick.

  Die Vorteile der SSD gegenüber der HDD sind folgende:

  Schnellere Datenauslese und Abspeicherung

  Die Daten sind sicherer, da es keinen sich bewegenden Lesekopf mehr gibt, der auf die Magnetscheibe prallen kann.

  Die Nachteile sind:

  Hoher Preis, wobei dieser langsam sinkt.
</p>
