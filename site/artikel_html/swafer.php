<p>
Als Wafer (englisch für „Waffel“ oder „Oblate“) werden in der Mikroelektronik, Photovoltaik und Mikrosystemtechnik kreisrunde oder quadratische, etwa ein Millimeter dicke Scheiben bezeichnet. <br>
Sie werden aus ein- oder polykristallinen (Halbleiter-)Rohlingen, sogenannten Ingots, hergestellt und dienen in der Regel als Substrat (Grundplatte) für elektronische Bauelemente, 
unter anderem für integrierte Schaltkreise (IC, „Chip“), mikromechanische Bauelemente oder photoelektrische Beschichtungen. Bei der Fertigung von mikroelektronischen Bauelementen werden in der
Regel mehrere Wafer zu einem Los zusammengefasst und direkt hintereinander oder auch parallel bearbeitet (vgl. Losfertigung).
Eine Scheibe besteht in den meisten Fällen aus monokristallinem Silicium,
es werden aber auch andere Materialien wie Siliciumcarbid, Galliumarsenid und Indiumphosphid verwendet. <br>
In der Mikrosystemtechnik werden auch Glas-Wafer mit einer Dicke im 1-mm-Bereich verwendet. 

Die Scheiben werden in verschiedenen Durchmessern gefertigt. Die zurzeit hauptsächlich verwendeten Wafer-Durchmesser unterscheiden sich je nach Halbleiterwerkstoff
und vorgesehenem Verwendungszweck (Silicium: 150 mm, 200 mm und 300 mm – 450 mm sind in der Diskussion.

Gallium-Arsenid: 2 Zoll, 3 Zoll, 100 mm, 125 mm und 150 mm – 200 mm technisch machbar). Je größer der Wafer, desto mehr ICs können darauf untergebracht werden. Da bei größeren
Wafern der geometrische Verschnitt kleiner wird, können die ICs kostengünstiger produziert werden.
Um die Ausbeute zu maximieren, werden die Wafer in Reinräumen produziert. 

