<h3>Funktionsweise</h3>
<p>
2003 brachte Microsoft die Smartwatch auf den Markt, die sogenannte "SPOT-Uhr", die sich aber nicht durchgesetzt hat.<br>
Danach kamen mehrere Smartwatches auf den Markt, wie der "Chronos" von Texas Instruments, aber auch der konnte den Durchbruch nicht schaffen.<br>
2013 war endlich das Jahr der Smartwatch, denn Samsung brachte ihre erste Smartwatch, den "Samsung Gear". Diese hatte dann schon viel mehr Erfolg.<br>
Im selbigem Jahr stellte auch Samsungs Rivale Apple ihre Smartwatch vor, diese nannten sie "Apple Watch".<br>
</p>
     
<h3>Verwendung</h3>
<p>
Das Prinzip war, dass man nicht immer sein Handy aus der Tasche holen sollte, wenn man einen Anruf oder eine SMS bekam.
Durch die aktuellen Funktionen kann man sogar auf der Smartwatch Videospiele spielen.<br>
</p>
     
