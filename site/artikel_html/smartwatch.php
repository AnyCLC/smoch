<h3>Aufbau</h3>
<p>
Smartwatch bedeutet so viel wie "schlaue Uhr" auf Englisch.<br>
Die Smartwatch ist eine Armbanduhr, die über Display, Sensoren und weitere Technische Funktionalitäten, so wie einen Internetzugriff verfügt.<br>
Auf den neuen Smartwatches kann man über zusätzliche Apps neue Funktionen installieren.<br>
</p>
