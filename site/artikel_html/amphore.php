<p class='zit copy'>
Heute werden Amphoren nur mehr zu Zierzwecken, beispielsweise als Vase, hergestellt. 
Eine besondere Rolle spielt die Amphore bis heute bei der Herstellung spezieller Weine, dem sogenannten „Amphorenwein“.
