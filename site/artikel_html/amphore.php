<h3>Funktionsweise</h3>
<p>
Die Amphore war auch ein Hohlmaß im römischen Reich. Eine Amphora betrug ca. 26 Liter.<br>
</p>

<h3>Verwendung</h3>
<p>
Amphoren wurden in der Antike als Speicher- und Transportgefäss für z. B. Wein, Oliven, Milch und Honig benutzt. Je nach Inhalt war das Volumen unterschiedlich. Das Fassungsvermögen betrug zwischen 5 und 50 Liter.<br>
Heute werden Amphoren als Vasen benutzt. Eine grosse Rolle spielt die Amphore bei der Herstellung spezieller Weine, wie z. B. "Amphorenwein".<br>
</p>

