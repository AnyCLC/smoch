                                                                 <h3>Funktionsweise</h3>
<p>
Indem die Kugeln von der einen Seite der Spalte zur anderen verschoben werden, kann mit einem Abakus addiert, subtrahiert, multipliziert sowie dividiert werden.<br>
Man kann sogar die Quadrat- und Kubikwurzeln ziehen, was schon wesentlich komplexer ist, als das Rechnen mit den vier Grundoperatoren.</p>
                                                                 
<h3>Verwendung</h3>
<p>
Der Abakus (lat. für die Tafel, das Brett) ist ein 3000 Jahre altes Rechenhilfsmittel und wurde vor allem bei kleinen Geschäften verwendet, da er leicht zu bedienen war.
</p>

                                                                 <h3>Fun Facts</h3>
                                                                 <p>Da man höhere Rechenoperationen, mit den römischen Zahlen nicht durchführen konnte, gab es im Römischen Reich eine leicht abgewandelte Version des Abakus.</p>

