<h3>Aufbau</h3>
<p>
Eine Elektronenröhre ist ein aktives elektrisches Bauelement mit Elektroden, die sich in einem evakuierten oder gasgefüllten Kolben aus Glas, Stahl oder Keramik befinden. 
Die Anschlüsse der Elektroden sind aus dem nach außen geführt. In ihrer einfachsten Form als Diode enthält eine Elektronenröhre eine beheizte Kathode (Glühkathode) und eine Anode. 
Elektronenröhren dienen zur Erzeugung, Gleichrichtung, Verstärkung oder Modulation elektrischer Signale.
</p>
