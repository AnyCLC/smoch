<h3>Funktionsweise</h3>
<p class="zit copy">
Hochleistungs-Hochfrequenzröhren als Senderöhren in der Radar- und Funktechnik sind jedoch bis heute die günstigste Möglichkeit, Hochfrequenz hoher Leistung zu erzeugen. 
Hier kommen Trioden, luft- und wassergekühlte Tetroden, Klystrons, Magnetrons und Wanderfeldröhren zum Einsatz. 
Solche leistungsfähigen Röhren werden in der Industrie unter anderem für die Hochfrequenzerwärmung eingesetzt. 
Das sind Anlagen, die kapazitiv oder induktiv Wärme direkt in einem Werkstück erzeugen. 
Weitere Anwendungen sind Hochfrequenzgeneratoren zur Plasmaerzeugung (Sputtern oder zur Anregung von Gaslasern). 
Magnetrons finden unter anderem im Mikrowellenherd und in Radar-Geräten weite Verwendung. </p>
