<h3>Funktionsweise</h3>
<p>
Röhrencomputer (Röhrenrechner), häufig auch als Computer, der ersten Generation bezeichnet, 
wurden von den 1940er bis Anfang der 1960er Jahre gebaut. Da ihre zentralen Schaltelemente im Gegensatz zu den heutigen hochintegrierten Transistoren Elektronenröhren waren, 
wurde ihre Elektronik als Röhrenelektronik oder Röhrentechnik bezeichnet.
</p>
<h3>Verwendung</h3>
<p>
Der Electronic Numerical Integrator and Computer (ENIAC) war der erste rein elektronische Universalrechner. Der ENIAC diente der US-Armee zur Berechnung ballistischer Tabellen. Die komplette Anlage war in U-Form aufgebaut, beanspruchte eine Fläche von 10 m × 17 m und wog 27 Tonnen. Er bestand aus 17.468 Elektronenröhren, 7.200 Dioden, 1.500 Relais, 70.000 Widerständen. 
</p>
