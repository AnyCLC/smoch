     <h3>Funktionsweise</h3>
<p class='zit copy'>
Eine Lochkarte ist ein aus Spezialpapier gefertigter, in der Datenverarbeitung zur Datenerfassung und -Speicherung verwendeter Datenträger. In ihm wurden die Dateninhalte durch einen Lochcode abgebildet, der mithilfe von elektro-mechanischen Geräten erzeugt und verarbeitet wurde. 
<p class='zit src'>
<i>Quelle: <a href="https://educalingo.com/de/dic-de/lochkarte">Educalingo</a> April 2018</i>

     <h3>Verwendung</h3>
<p class='zit copy'>
In den Anfangszeiten der elektronischen Datenverarbeitung konnten vielen Computersystemen ihre Eingabedaten ausschließlich über Lochkarten 
(oder Lochstreifen) zugeführt werden. Auch zur Speicherung von Bestandsdaten (z. B. Kontoinformationen einer Bank) wurden zunächst Lochkarten verwendet, 
bis die Speicherung und Verarbeitung auf magnetischen Datenträgern effizienter und günstiger wurde. In der Softwareentwicklung wurde der Programm-Quellcode 
von den Programmierern auf Lochkarten erfasst, durch Übersetzer verarbeitet und auch als Lochkarten archiviert. Auch zum Laden von Programmen 
(im Maschinencode, zum Teil auch als Programm-Quelltext), wurden Lochkarten verwendet.
