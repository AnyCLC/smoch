     <h3>Funktionsweise</h3>
<p>
Eine Lochkarte ist ein Spezialpapier gefertigter Datenträger, auf dem Datenerfassung und Speicherungen gemacht wurden. Im Papier wurden die Dateninhalte mit einem Lochcode abgebildet, die von elektro-mechanischen Geräten erzeugt und verarbeitet wurden.<br>
</p>

     <h3>Verwendung</h3>
<p>
In den Anfangszeiten der elektronischen Datenverarbeitung konnten viele Computersysteme ihre Eingabedaten ausschliesslich auf Lochkarten speichern.<br>
Solange die Speicherung auf magnetischen Datenträgern nicht günstiger wurden, wurden Lochkarten auch zur Speicherung von Daten in Banken verwendet.<br>
Sogar zum Laden von Programmen wurden Lochkarten verwendet.<br>
</p>

     <h3>Geschichte</h3>
     Der Erfinder der Lochkarte ist Herman Hollerit. Er liess seine Erfindung 1884 patentieren.<br>
Er experimentierte in den folgenden Jahren mit verschiedenen Lochkartenformaten und Anordnungen.<br>


     
