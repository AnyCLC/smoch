<i>Quelle: <a href="https://de.wikipedia.org/wiki/Lochkarte">Wikipedia</a> April 2018</i>

     <h3>Geschichte</h3>
<p class='zit copy'>
Am 23. September 1884 reichte Herman Hollerith seine erste Patentanmeldung zum Thema „Art of Compiling Statistics“ ein. 
Er experimentierte in den Folgejahren mit verschiedenen Lochkartenformaten und Anordnungen.
