<h3>Aufbau</h3>
<p>
Das Magnetband ist ein alter Datenträger. Es besteht aus einer langen, schmalen Folie aus Kunststoff.<br>
Das Band ist auf Wickelkernen, auch "Bobbys" genannt, gewickelt.<br>
