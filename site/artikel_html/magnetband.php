<h3>Funktionsweise</h3>
<p>
Magnetbänder waren die ersten Datenträger, die die Massenspeicherung von grossen Ton- und Filmaufnahmen ermöglichten.<br>
Durch die unzähligen kleinen Magnetnadeln kann das Magnetband binäre Informationen speichern.<br>
Die Informationen waren dann "dauerhaft" gespeichert, bis es zu einer Neuausrichtung der Nadeln kam.<br>
Wenn man eine grosse Datenmenge hat, eignet sich die Verwendung des Magnetbandes nicht.<br>
</p>
<h3>Verwendung</h3>
<p>
Magnetbänder dienten als Datenspeicher der elektronischen Datenverarbeitung neben Trommelspeichern bei den Grosscomputern der 1950er und 1960er Jahre.<br>
Magnetbänder gibt es in sehr vielen Formen und sie dienen auch heute noch der digitalen Darstellung von Video- und Audio sowie allgemein digitalen Informationen.<br>
</p>
