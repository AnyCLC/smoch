<h3>Verwendung</h3>
<p class='zit copy'>
Eine der häufigsten Anwendungen des USB-Stick sind USB-Speichersticks (kurz „USB-Sticks“), eine Form der USB-Massenspeicher.
Diese Wechseldatenträger nutzen Flash-Speicher zur Speicherung der Daten.
Manche portable Geräte mit Datenspeicher können auch als Speicherstick verwendet werden oder nutzen diese Sichtweise zur Datenübertragung auf einen Computer,
beispielsweise MP3-Player, Digitalkameras oder Smartphones. Verfügbar sind auch kleine Speicherkartenleser,
die häufig als „Adapter“ für Speicherkarten oder als Speichersticks mit austauschbarem bzw. erweiterbarem Speicher vermarktet werden.
Ein weiterer großer Anwendungsbereich sind Adapter für den drahtlosen Datenaustausch.
Hier wird eine Vielzahl unterschiedlicher Protokolle und Standards unterstützt.
Zu nennen sind vor allem Adapter für den Anschluss an ein lokales Funknetzwerk („Wireless-Adapter, WLAN-Stick“)
oder für den Internetanschluss über das Mobilfunknetz („UMTS-Stick“, siehe UMTS-Modem).
Darüber hinaus existieren Adapter für den Datenaustausch
über kurze Distanz per Infrarot (z. B. IrDA) oder Bluetooth bis hin zu
einfachen Empfängern für drahtlose Mäuse und Tastaturen.</p>
