     <h3>Funktionsweise</h3>
<p class='zit copy'>
  Wichtig um zu Verstehen, wie Daten auf dem USB-Stick gespeichert und abgerufen werden können, ist der Aufbau des Speichers:
In jedem USB-Stick befinden sich sogenannte Flash-Speicherzellen, die wie Transistoren funktionieren. Zum größten Teil bestehen sie aus einem Halbleiter-Material,
zum Beispiel aus Silizium. Darin befinden sich zwei kleinere Bereiche aus viel leitfähigerem Material, Source (Quelle) und Drain (Abfluss).<br>
Durch einen Isolator getrennt vom Rest, befindet sich zwischen Source und Drain das Floating-Gate (etwa: Schwebe-Tor).
Dieses Bauteil ist der eigentliche Speicherort der Flash-Zelle. Über das Steuer-Gate, das mit dem Floating-Gate verbunden ist,
kann eine postive oder negative Spannung angelegt werden.<br>
Wird nun an das Steuer-Gate eine positive Spannung angelegt (etwa 3,3 Volt),
entsteht ein Kanal zwischen Source und Drain, es fließt Strom. Dieser Zustand wird als 0-Zustand bezeichnet.<br>
Jede der Zellen kann entweder im 0- oder im 1- Zustand sein. Aus sehr vielen dieser Zellen kann ein Computer dann gespeicherte Daten auslesen.
</p>
