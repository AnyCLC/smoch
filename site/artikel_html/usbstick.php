<h3> Aufbau </h3>
<p class='zit copy'>
  Ein USB-Stick (engl. Stick = Stab, Stock) bezeichnet allgemein ein kompakt gebautes elektronisches Gerät, das über den Universal Serial Bus (USB) mit einem anderen Gerät,
  beispielsweise einem PC verbunden wird. Dabei sind der USB-Stecker (meist Typ A) und das Gehäuse direkt miteinander verbunden und bilden eine mechanische Einheit ohne verbindendes Kabel.
  Im Speziellen bezeichnet USB-Stick die häufigste Ausprägung des Gerätes, den USB-Speicherstick.<a href="https://de.wikipedia.org/wiki/USB-Stick#cite_note-1">[1]</a>
  Die Miniaturisierung der USB-Sticks geht seit etwa 2010 so weit, dass der ursprüngliche Stick auf eine Platine reduziert werden kann,
  die vollständig in einen USB-Anschluss eingeschoben werden kann. Die meisten Geräte sind jedoch aus Gründen der Handhabung entweder etwas länger gestaltet oder besitzen die Form
  eines USB-Steckers mit kurzem Griff, um das Verlustrisiko zu verringern.
