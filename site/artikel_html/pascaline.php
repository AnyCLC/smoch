     <h3>Aufbau</h3>
     <p class='zit copy'>Die Pascaline ist eine mechanische Rechenmaschine, die Metallwählscheiben aufwies, auf denen die gewünschten Nummern ausgewählt werden konnten. Dies waren ähnliche Scheiben, wie damals bei den Telefonen Verwendung fanden. Das Ergebnis erschien in den kleinen Kästchen über den Wählscheiben.
    Eine direkte Substraktion war nicht möglich, dafür musste die Komplementärmethode verwendet werden.</p>
     <p class='zit src'>Wikipedia 2018</p>
     
<h3>Funktionsweise</h3>
    <p>An Metallwählscheiben wurden die gewünschten Zahlen eingestellt.</p>
    
     <h3>Verwendung</h3>
     <p>Die Pascaline wurde zur Addition großer und vieler Zahlen verwendet.</p>

     
<h3> Fun Facts </h3>
    <p class='zit copy'>Die Pascaline wurde u. a. aus Messing, Elfenbein und Holz gefertigt. Im Laufe seines Lebens verfeinerte Pascal den Mechanismus immer wieder und fertigte so ca. 50 Versionen der Pascaline an.</p>
    <p class='zit src'>Wikipedia 2018</p>

    <h4>Geschichte</h4>
<p class='zit copy'>Blaise Pascal (1623-1662) begann mit der Arbeit an seiner Rechenmaschine als er 19 Jahre alt war. Er konstruierte sie als Arbeitserleichterung für seinen Vater, welcher Steuerbeamter war.
    Der erste Prototyp hatte nur wenige Wählscheiben, spätere Versionen hatten mehr sodass bis zur Zahl 9'999'999 angezeigt werden konnte.</p>
     <p class='zit src'>Wikipedia 2018</p>

