<h3>Geschichte</h3>

<p>Blaise Pascal (1623-1662) begann mit der Arbeit an seiner Rechenmaschine als er 19 Jahre alt war. Er konstruierte sie als Arbeitserleichterung für seinen Vater, welcher Steuerbeamter war.
Der erste Prototyp hatte nur wenige Wählscheiben, spätere Versionen hatten mehr sodass bis zur Zahl 9'999'999 angezeigt werden konnte.</p>

