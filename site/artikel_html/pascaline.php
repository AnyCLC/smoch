     <h3>Aufbau</h3>
     <p>Die Pascaline ist eine mechanische Rechenmaschine, die Metallwählscheiben aufwies, auf denen die gewünschten Nummern ausgewählt werden konnten. Dies waren ähnliche Scheiben, wie damals bei den Telefonen Verwendung fanden. Das Ergebnis erschien in den kleinen Kästchen über den Wählscheiben.
Eine direkte Substraktion war nicht möglich, dafür musste die Komplementärmethode verwendet werden.</p>
                                                                                                                                                             
<h3> Fun Facts </h3>
<p>Die Pascaline wurde u. a. aus Messing, Elfenbein und Holz gefertigt. Im Laufe seines Lebens verfeinerte Pascal den Mechanismus immer wieder und fertigte so ca. 50 Versionen der Pascaline an.</p>
